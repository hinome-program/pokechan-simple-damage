// ============================================================
// 相性計算関数
//   attackType  : 攻撃技のタイプ名（例: 'ほのお'）
//   defTypes    : 防御側タイプ配列（例: ['くさ', 'どく']）
//   戻り値: 0 | 0.25 | 0.5 | 1 | 2 | 4
// ============================================================
function calcTypeMultiplier(attackType, defTypes) {
    const atkIdx = TYPE_LIST.indexOf(attackType);
    if (atkIdx === -1) return 1; // タイプ不明 → ×1

    let mult = 1;
    for (const dt of defTypes) {
        const defIdx = TYPE_LIST.indexOf(dt);
        if (defIdx === -1) continue;
        mult *= TYPE_CHART_RAW[atkIdx][defIdx];
    }
    return mult;
}

