def fetch_stats(pokemon_id):
    url = f"https://pokeapi.co/api/v2/pokemon/{pokemon_id}"
    req = urllib.request.Request(url, headers={"User-Agent": "poketyan-build/1.0"})
    with urllib.request.urlopen(req, timeout=10) as resp:
        data = json.loads(resp.read().decode())
    def g(name):
        for s in data["stats"]:
            if s["stat"]["name"] == name:
                return s["base_stat"]
        return 0
    return {
        "hp":  g("hp"),
        "atk": g("attack"),
        "def": g("defense"),
        "spa": g("special-attack"),
        "spd": g("special-defense"),
        "spe": g("speed"),
    }

def main():
    print(f"\n🔥 PokeAPI から {len(roster)} 体分の種族値を取得します...\n")
    cache   = {}
    results = []
    errors  = 0

    for i, (name, pid) in enumerate(roster):
        key = str(pid)
        if key in cache:
            bs = cache[key]
            results.append({"name": name, "id": pid, "baseStats": bs})
            print(f"  [cache] {name}")
            continue

        try:
            sys.stdout.write(f"  [{i+1:3d}/{len(roster)}] Fetching {name} ({pid})... ")
            sys.stdout.flush()
            bs = fetch_stats(pid)
            cache[key] = bs
            results.append({"name": name, "id": pid, "baseStats": bs})
            print(f"HP:{bs['hp']} ATK:{bs['atk']} DEF:{bs['def']} SPA:{bs['spa']} SPD:{bs['spd']} SPE:{bs['spe']}")
            time.sleep(0.15)  # レートリミット回避
        except Exception as e:
            print(f"ERROR: {e}")
            results.append({"name": name, "id": pid, "baseStats": None})
            errors += 1

    # pokemon_data.js として出力（window.POKEMON_DATA グローバル変数）
    js_content = f"""// pokemon_data.js — PokeAPIから自動生成（generate_pokemon_data.py）
// Generated: {datetime.now().isoformat()}
// {len(results)} Pokemon / {errors} errors
/* global window */
window.POKEMON_DATA = {json.dumps(results, ensure_ascii=False, indent=2)};
"""
    with open("./pokemon_data.js", "w", encoding="utf-8") as f:
        f.write(js_content)

    print(f"\n✅ 完了: pokemon_data.js を生成しました")
    print(f"   {len(results)} 体登録 / エラー {errors} 件")
    if errors > 0:
        print("   ⚠️  エラーがあったポケモンは baseStats が null で記録されています")

if __name__ == "__main__":
    main()
